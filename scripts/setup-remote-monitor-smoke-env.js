#!/usr/bin/env node
const path = require('path');

module.paths.push(path.join(__dirname, 'node_modules'));

const REMOTE_ROOT = '/data/crosshub';
const REMOTE_ENV = `${REMOTE_ROOT}/.monitor-smoke.env`;

function shellQuote(value) {
  return `'${String(value).replace(/'/g, `'\\''`)}'`;
}

function shellEnvValue(value) {
  const normalized = String(value).replace(/\r?\n/g, '');
  if (/^[A-Za-z0-9_./:@+-]*$/.test(normalized)) {
    return normalized;
  }
  return `'${normalized.replace(/'/g, `'\\''`)}'`;
}

function envLine(name, value) {
  return `${name}=${shellEnvValue(value)}`;
}

function buildMonitorSmokeEnv(env = process.env) {
  const lines = [
    '# Generated by scripts/setup-remote-monitor-smoke-env.js',
    '# Private server file. Do not commit.',
  ];
  if (env.CROSSHUB_MONITOR_API_TOKEN) {
    lines.push(envLine('CROSSHUB_MONITOR_API_TOKEN', env.CROSSHUB_MONITOR_API_TOKEN));
  } else {
    lines.push(envLine('CROSSHUB_MONITOR_LOGIN_ACCOUNT', env.CROSSHUB_MONITOR_LOGIN_ACCOUNT || ''));
    lines.push(envLine('CROSSHUB_MONITOR_LOGIN_PASSWORD', env.CROSSHUB_MONITOR_LOGIN_PASSWORD || ''));
    lines.push(envLine('CROSSHUB_MONITOR_PORTAL_ROLE', env.CROSSHUB_MONITOR_PORTAL_ROLE || 'boss'));
  }
  lines.push(envLine('CROSSHUB_MONITOR_SMOKE_TIMEOUT_MS', env.CROSSHUB_MONITOR_SMOKE_TIMEOUT_MS || '60000'));
  return `${lines.join('\n')}\n`;
}

function validateConfig(env = process.env) {
  const missing = [];
  if (!env.CROSSHUB_SSH_HOST) missing.push('CROSSHUB_SSH_HOST');
  if (!env.CROSSHUB_SSH_PASSWORD) missing.push('CROSSHUB_SSH_PASSWORD');
  if (!env.CROSSHUB_MONITOR_API_TOKEN && !(env.CROSSHUB_MONITOR_LOGIN_ACCOUNT && env.CROSSHUB_MONITOR_LOGIN_PASSWORD)) {
    missing.push('CROSSHUB_MONITOR_API_TOKEN or CROSSHUB_MONITOR_LOGIN_ACCOUNT/CROSSHUB_MONITOR_LOGIN_PASSWORD');
  }
  return { ok: missing.length === 0, missing };
}

function connectSsh(env = process.env) {
  const { Client } = require('ssh2');
  const conn = new Client();
  const config = {
    host: env.CROSSHUB_SSH_HOST,
    port: Number(env.CROSSHUB_SSH_PORT || 22),
    username: env.CROSSHUB_SSH_USER || 'root',
    password: env.CROSSHUB_SSH_PASSWORD,
    readyTimeout: 120000,
  };
  return new Promise((resolve, reject) => {
    conn.on('ready', () => resolve(conn)).on('error', reject).connect(config);
  });
}

function exec(conn, cmd) {
  return new Promise((resolve, reject) => {
    conn.exec(cmd, (err, stream) => {
      if (err) return reject(err);
      stream.stderr.on('data', (d) => process.stderr.write(d));
      stream.on('close', (code) => {
        if (code) reject(new Error(`remote exit ${code}`));
        else resolve();
      });
    });
  });
}

function writeRemoteFile(conn, remotePath, content) {
  return new Promise((resolve, reject) => {
    conn.sftp((err, sftp) => {
      if (err) return reject(err);
      const stream = sftp.createWriteStream(remotePath, { mode: 0o600, encoding: 'utf8' });
      stream.on('error', reject);
      stream.on('close', resolve);
      stream.end(content);
    });
  });
}

async function setupRemoteMonitorSmokeEnv(env = process.env) {
  const validation = validateConfig(env);
  if (!validation.ok) {
    throw new Error(`missing ${validation.missing.join(', ')}`);
  }
  const content = buildMonitorSmokeEnv(env);
  const conn = await connectSsh(env);
  try {
    await exec(conn, `mkdir -p ${shellQuote(REMOTE_ROOT)}`);
    await writeRemoteFile(conn, REMOTE_ENV, content);
    await exec(conn, `chmod 600 ${shellQuote(REMOTE_ENV)}`);
    return { ok: true, remotePath: REMOTE_ENV };
  } finally {
    conn.end();
  }
}

async function main() {
  try {
    const result = await setupRemoteMonitorSmokeEnv(process.env);
    console.log(`remote_monitor_smoke_env_written=${result.remotePath}`);
    console.log('Set CROSSHUB_REMOTE_MONITOR_SMOKE_ENV_READY=true before running deploy-preflight/deploy-server.');
  } catch (error) {
    console.error(error.message || error);
    process.exit(1);
  }
}

if (require.main === module) {
  main();
}

module.exports = {
  buildMonitorSmokeEnv,
  shellEnvValue,
  setupRemoteMonitorSmokeEnv,
  validateConfig,
};
